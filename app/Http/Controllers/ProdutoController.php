    {

        $produtos = DB::table('produtos')->get();

        // Formata o valor da moeda para o tipo PT-BR
        foreach ($produtos as $produto) {
            $produto->valor = number_format($produto->valor, 2, ',', '.');
        }

        return view('listar', ['produtos' => $produtos]);
    }

